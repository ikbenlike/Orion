#include <ctypes.h>

int tolower(int c){
    switch(c){
        case 'A':
            return (int)'a';
            break;
        case 'B':
            return (int)'b';
            break;
        case 'C':
            return (int)'c';
            break;
        case 'D':
            return (int)'d';
            break;
        case 'E':
            return (int)'e';
            break;
        case 'F':
            return (int)'f';
            break;
        case 'G':
            return (int)'g';
            break;
        case 'H':
            return (int)'h';
            break;
        case 'I':
            return (int)'i';
            break;
        case 'J':
            return (int)'j';
            break;
        case 'K':
            return (int)'k';
            break;
        case 'L':
            return (int)'l';
            break;
        case 'M':
            return (int)'m';
            break;
        case 'N':
            return (int)'n';
            break;
        case 'O':
            return (int)'o';
            break;
        case 'P':
            return (int)'p';
            break;
        case 'Q':
            return (int)'q';
            break;
        case 'R':
            return (int)'r';
            break;
        case 'S':
            return (int)'s';
            break;
        case 'T':
            return (int)'t';
            break;
        case 'U':
            return (int)'u';
            break;
        case 'V':
            return (int)'v';
            break;
        case 'W':
            return (int)'w';
            break;
        case 'X':
            return (int)'x';
            break;
        case 'Y':
            return (int)'y';
            break;
        case 'Z':
            return (int)'z';
            break;
        default:
            return c;
            break;
    }
    return c;
}
