#include <ctypes.h>

int toupper(int c){
    switch(c){
        case 'a':
            return (int)'A';
            break;
        case 'b':
            return (int)'B';
            break;
        case 'c':
            return (int)'C';
            break;
        case 'd':
            return (int)'D';
            break;
        case 'e':
            return (int)'E';
            break;
        case 'f':
            return (int)'F';
            break;
        case 'g':
            return (int)'G';
            break;
        case 'h':
            return (int)'H';
            break;
        case 'i':
            return (int)'I';
            break;
        case 'j':
            return (int)'J';
            break;
        case 'k':
            return (int)'K';
            break;
        case 'l':
            return (int)'L';
            break;
        case 'm':
            return (int)'M';
            break;
        case 'n':
            return (int)'N';
            break;
        case 'o':
            return (int)'O';
            break;
        case 'p':
            return (int)'P';
            break;
        case 'q':
            return (int)'Q';
            break;
        case 'r':
            return (int)'R';
            break;
        case 's':
            return (int)'S';
            break;
        case 't':
            return (int)'T';
            break;
        case 'u':
            return (int)'U';
            break;
        case 'v':
            return (int)'V';
            break;
        case 'w':
            return (int)'W';
            break;
        case 'x':
            return (int)'X';
            break;
        case 'y':
            return (int)'Y';
            break;
        case 'z':
            return (int)'Z';
            break;
        default:
            return c;
            break;
    }
    return c;
}
